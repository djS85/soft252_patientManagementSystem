    public Prescriptions getPrescriptions() {
        
        ArrayList<Prescription> _prescriptions = new ArrayList<>();
        
        if ( gson == null ) {
            gson = new GsonBuilder().setPrettyPrinting().create();
        }
        
        try ( FileReader fr = new FileReader(Files.PRESCRIPTIONS_PATH) ) {
            _prescriptions = gson.fromJson(fr, new TypeToken<ArrayList<Prescription>>() {}.getType());
        } catch (IOException ex) {
            ex.printStackTrace();
        }
        
        Prescriptions prescriptions = new Prescriptions();
        prescriptions.setPrescriptions(_prescriptions);
        
        return prescriptions;
    
    }
    
    public Medicines getMedicines() {
        
        ArrayList<Medicine> _medicines = new ArrayList<>();
        
        if ( gson == null ) {
            gson = new GsonBuilder().setPrettyPrinting().create();
        }
        
        try ( FileReader fr = new FileReader(Files.STOCK_PATH) ) {
            _medicines = gson.fromJson(fr, new TypeToken<ArrayList<Medicine>>() {}.getType());
        } catch (IOException ex) {
            ex.printStackTrace();
        }
        
        Medicines medicines = new Medicines();
        medicines.setMedicines(_medicines);
        
        return medicines;
    
    }
    
