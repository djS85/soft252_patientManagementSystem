    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String patientID = jTextField1.getText().toString();
        
        ArrayList<Prescription> prescriptions = this.user.Prescriptions().getPrescriptions();
        
        jTextArea1.setText("");
        
        if ( prescriptions.size() > 0 ) {
            for ( Prescription p : prescriptions ) {
                if ( p.getPatientID().equalsIgnoreCase(patientID) ) {
                    jTextArea1.append("Patient ID : " + p.getPatientID() + "\n"
                    + "Medicine : " + p.getMedicine() + "\n"
                    + "Quantity : " + p.getQuantity() + "\n\n");
                }
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        jLabel5.setText("");
        
        String medicine = jTextField2.getText().toString();
        
        int qty = 0;
        
        if ( !jTextField3.getText().isEmpty() ) {
            qty = Integer.valueOf(jTextField3.getText());
        }
        
        for ( Medicine m : this.user.Medicines().getMedicines() ) {
            if ( m.getMedicineName().equalsIgnoreCase(medicine) ) {
                if ( m.getQuantityInStock() > 0 ) {
                    int stock = m.getQuantityInStock();
                    stock -= qty;
                    m.setQuantityInStock(stock);
                } else {
                    jLabel5.setText(m.getMedicineName() + " is out of stock.");
                }
            } else {
                jLabel5.setText(medicine + " cannot be found.");
            }
        }
        
        this.user.writeMedicines(this.user.Medicines());
        
    }//GEN-LAST:event_jButton2ActionPerformed


